package sample.models.scales;

/**
 * enum for Scale types.
 */
public enum ScaleType {
    /**
     * Scale type for Major Scales.
     */
    MAJOR_SCALE,
    /**
     * Scale type for Minor Scales.
     */
    MINOR_SCALE,
    /**
     * Scale type for Major Pentatonic Scales.
     */
    MAJOR_PENTATONIC,
    /**
     * Scale type for Minor Pentatonic Scales.
     */
    MINOR_PENTATONIC
}
